package ltd.newbee.mall.entity;

public class RestaurantMenuDrink {
	
	private long restaurantId;
	private String restaurantName;
	private long drinkGenreId;
	private String drinkGenreName;
	private long drinkId;
	private String drinkName;
	private long drinkPrice;
	private String drinkRemark;
	
	public long getRestaurantId() {
		return restaurantId;
	}
	public void setRestaurantId(long restaurantId) {
		this.restaurantId = restaurantId;
	}
	public String getRestaurantName() {
		return restaurantName;
	}
	public void setRestaurantName(String restaurantName) {
		this.restaurantName = restaurantName;
	}
	public long getDrinkGenreId() {
		return drinkGenreId;
	}
	public void setDrinkGenreId(long drinkGenreId) {
		this.drinkGenreId = drinkGenreId;
	}
	public String getDrinkGenreName() {
		return drinkGenreName;
	}
	public void setDrinkGenreName(String drinkGenreName) {
		this.drinkGenreName = drinkGenreName;
	}
	public long getDrinkId() {
		return drinkId;
	}
	public void setDrinkId(long drinkId) {
		this.drinkId = drinkId;
	}
	public String getDrinkName() {
		return drinkName;
	}
	public void setDrinkName(String drinkName) {
		this.drinkName = drinkName;
	}
	public long getDrinkPrice() {
		return drinkPrice;
	}
	public void setDrinkPrice(long drinkPrice) {
		this.drinkPrice = drinkPrice;
	}
	public String getDrinkRemark() {
		return drinkRemark;
	}
	public void setDrinkRemark(String drinkRemark) {
		this.drinkRemark = drinkRemark;
	}
	
}
